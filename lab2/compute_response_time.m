function response_time = compute_response_time(t, y)
    % Находим момент времени, когда процесс достигает 90% от установившегося значения
    y_ss = y(end);
    y_90 = 0.9 * y_ss;

    idx_90 = find(y >= y_90, 1, 'first');

    % Находим момент времени, когда процесс достигает 10% от установившегося значения
    y_10 = 0.1 * y_ss;
    idx_10 = find(y >= y_10, 1, 'first');

    % Вычисляем временной интервал между 10% и 90%
    response_time = t(idx_90) - t(idx_10);
end